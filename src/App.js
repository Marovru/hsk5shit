function App() {
  const [currentTicketIndex, setCurrentTicketIndex] = useState(null);
  const [currentQuestionIndex, setCurrentQuestionIndex] = useState(0);
  const [score, setScore] = useState(0);
  const [showResult, setShowResult] = useState(false);

  const handleAnswer = (selectedOptionIndex) => {
    const currentTicket = quizData[currentTicketIndex];
    const currentQuestion = currentTicket.questions[currentQuestionIndex];

    if (selectedOptionIndex === currentQuestion.answer) {
      setScore(score + 1);
    }

    const nextQuestionIndex = currentQuestionIndex + 1;
    if (nextQuestionIndex < currentTicket.questions.length) {
      setCurrentQuestionIndex(nextQuestionIndex);
    } else {
      setShowResult(true);
    }
  };

  const restartQuiz = () => {
    setScore(0);
    setCurrentQuestionIndex(0);
    setShowResult(false);
    setCurrentTicketIndex(null);
  };

  return (
    <div
      style={{
        padding: "110px",
        fontFamily: "Arial, sans-serif",
      }}
    >
      {/* Контейнер для Quiz App */}
      <div
        style={{
          textAlign: "center", // Центрирование текста
          marginBottom: "45px", // Отступ вниз
        }}
      >
        <h1 style={{ margin: 0 }}>加油</h1>
      </div>

      {currentTicketIndex === null ? (
        <div>
          {/* Контейнер для заголовка "Билеты" */}
          <div
            style={{
              textAlign: "center",
              marginBottom: "0px",
              position: "relative", // Делаем заголовок позиционируемым
              left: "0", // По желанию, можно двигать с помощью left
            }}
          >
            <h2>HSK5级:</h2>
          </div>

          {/* Контейнер для кнопок "Билеты" */}
          <div
            style={{
              display: "flex",
              flexDirection: "column",
              alignItems: "center", // Центрирование кнопок по горизонтали
              justifyContent: "center", // Центрирование по вертикали
              position: "relative", // Делаем кнопки позиционируемыми
              marginTop: "20px",
            }}
          >
            {quizData.map((ticket, index) => (
              <button
                key={index}
                onClick={() => setCurrentTicketIndex(index)}
                style={{
                  margin: "7px 0", // Отступ между кнопками
                  padding: "15px 0", // Вертикальный отступ внутри кнопок
                  width: "170px", // Фиксированная ширина кнопок
                  backgroundColor: "#007bff",
                  color: "#fff",
                  border: "none",
                  borderRadius: "7px",
                  cursor: "pointer",
                  textAlign: "center", // Центрирование текста внутри кнопки
                  position: "relative", // Делаем кнопки позиционируемыми
                  left: "0", // Здесь можно сдвигать кнопки
                }}
              >
                Ticket {ticket.ticket}
              </button>
            ))}
          </div>
        </div>
      ) : showResult ? (
        <div>
          {/* Контейнер для текста "Your Score" */}
          <div
            style={{
              marginTop: "200px",
              marginBottom: "20px",
              textAlign: "center", // Центрирование
              position: "relative", // Делаем текст позиционируемым
              left: "0", // Даем возможность сдвигать
            }}
          >
            <h2>
              Your Score: {score} / {quizData[currentTicketIndex].questions.length}
            </h2>
          </div>

          {/* Контейнер для кнопки "RESTART HSK5" */}
          <div
            style={{
              textAlign: "center", // Центрирование
              marginTop: "3%", // Сдвиг кнопки
              position: "relative", // Делаем кнопку позиционируемой
              left: "0", // Местоположение кнопки
            }}
          >
            <button
              onClick={restartQuiz}
              style={{
                padding: "10px 20px",
                fontSize: "30px",
                backgroundColor: "#007bff",
                color: "#fff",
                border: "none",
                borderRadius: "7px",
                cursor: "pointer",
                position: "relative", // Делаем кнопку позиционируемой
                left: "0", // По желанию можно сдвигать
              }}
            >
              RESTART HSK5
            </button>
          </div>
        </div>
      ) : (
        <div>
          <h2
            style={{
              textAlign: "center", // Центрирование текста
            }}
          >
            Ticket {quizData[currentTicketIndex].ticket} - Question{" "}
            {currentQuestionIndex + 1} /{" "}
            {quizData[currentTicketIndex].questions.length}
          </h2>
          <p
            style={{
              textAlign: "center", // Центрирование текста вопроса
            }}
          >
            {quizData[currentTicketIndex].questions[currentQuestionIndex].question}
          </p>
          <div
            style={{
              textAlign: "center", // Центрирование кнопок с ответами
            }}
          >
            {quizData[currentTicketIndex].questions[currentQuestionIndex].options.map(
              (option, index) => (
                <button
                  key={index}
                  onClick={() => handleAnswer(index)}
                  style={{
                    display: "inline-block", // Выводим кнопки в линию
                    margin: "15px", // Отступы между кнопками
                    padding: "10px 30px",
                    backgroundColor: "#007bff",
                    color: "#fff",
                    border: "none",
                    borderRadius: "10px",
                    cursor: "pointer",
                    position: "relative", // Делаем кнопки позиционируемыми
                    left: "0", // По желанию можно сдвигать
                  }}
                >
                  {option}
                </button>
              )
            )}
          </div>
        </div>
      )}
    </div>
  );
}

export default App;